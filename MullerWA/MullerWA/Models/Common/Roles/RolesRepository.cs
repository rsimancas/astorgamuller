        private Roles Get(int id, SqlConnection oConn)
        {
            string sql = "SELECT * FROM Roles " +
                         " WHERE (RoleId = @id)";

            SqlDataAdapter da = new SqlDataAdapter(sql, oConn);

            da.SelectCommand.Parameters.Add("@id", SqlDbType.Int).Value = Convert.ToInt32(id);

            DataSet ds = new DataSet();

            da.Fill(ds);

            DataTable dt;
            dt = ds.Tables[0];

            if (dt.Rows.Count > 0)
            {
                IList<Roles> data = EnumExtension.ToList<Roles>(dt);
                return data.FirstOrDefault<Roles>();
            }

            return null;
        }

