        private Tabulador Get(int id, SqlConnection oConn)
        {
            string sql = "SELECT * FROM Tabulador " +
                         " WHERE (TabId = @id)";

            SqlDataAdapter da = new SqlDataAdapter(sql, oConn);

            da.SelectCommand.Parameters.Add("@id", SqlDbType.Int).Value = Convert.ToInt32(id);

            DataSet ds = new DataSet();

            da.Fill(ds);

            DataTable dt;
            dt = ds.Tables[0];

            if (dt.Rows.Count > 0)
            {
                IList<Tabulador> data = EnumExtension.ToList<Tabulador>(dt);
                return data.FirstOrDefault<Tabulador>();
            }

            return null;
        }

        public bool Remove(Tabulador estatus, ref string msgError)
